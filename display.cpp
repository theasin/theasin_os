    void term_print(str string, uint8_t cl)
    {
        for (size_t i = 0; string[i] != '\0'; i++)
            term_putc(string[i], cl);
    }


    void term_printat(str st, uint8_t cl, uint8_t x, uint8_t y)
    {
        uint8_t buff_x = term_col;
        uint8_t buff_y = term_row;

        term_col = x;
        term_row = y;

        term_print(st, cl);

        term_col = buff_x;
        term_row = buff_y;
    }

